<p>Pour créer des comptes locaux, il faut importer&nbsp;:</p>
<ul>
<li>Pour les élèves&nbsp;: Le fichier BEE,</li>
<li>Pour les enseignants&nbsp;: Le fichier STS.</li>
</ul>
<p>&nbsp;</p>
<h5>Fichier BEE (Base Élève Établissement)</h5>
<p>Se connecter à SIECLE BEE&nbsp;:</p>
<p><img style = "border: 1px solid;" src='./ents/help/bee.jpg' /></p>
<ol>
<li>Cliquer sur "Exportations",</li>
<li>Cliquer sur "En XML",</li>
<li>Cliquer sur "Elèves sans adresse".</li>
</ol>
<p>Cela déclenche le téléchargement du fichier.</p>
<p>NOTE&nbsp;: il est inutile de dézipper ce fichier.</p>

<p>&nbsp;</p>
<h5>Fichier STS</h5>
<p>Se connecter à STS&nbsp;:</p>
<p><img style = "border: 1px solid;" src='./ents/help/sts.jpg' /></p>
<ol>
<li>Cliquer sur "Exports",</li>
<li>Cliquer sur "Emploi du temps".</li>
</ol>
<p>Cela déclenche le téléchargement du fichier.</p>